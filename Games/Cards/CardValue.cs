namespace ShooShosha.Games.Cards
{
    /// <summary>
    /// Represents a playing card value.
    /// </summary>
    /// <remarks>
    /// In this definition, <see cref="Ace"/> is low.
    /// </remarks>
    public enum CardValue
    {
        /// <summary>
        /// The ace card.
        /// </summary>
        Ace = 1,

        /// <summary>
        /// The two (2) card.
        /// </summary>
        Two,

        /// <summary>
        /// The three (3) card.
        /// </summary>
        Three,

        /// <summary>
        /// The four (4) card.
        /// </summary>
        Four,

        /// <summary>
        /// The five (5) card.
        /// </summary>
        Five,

        /// <summary>
        /// The six (6) card.
        /// </summary>
        Six,

        /// <summary>
        /// The seven (7) card.
        /// </summary>
        Seven,

        /// <summary>
        /// The eight (8) card.
        /// </summary>
        Eight,

        /// <summary>
        /// The nine (9) card.
        /// </summary>
        Nine,

        /// <summary>
        /// The ten (10) card.
        /// </summary>
        Ten,

        /// <summary>
        /// the jack card.
        /// </summary>
        Jack,

        /// <summary>
        /// The queen card.
        /// </summary>
        Queen,

        /// <summary>
        /// The king card.
        /// </summary>
        King
    }
}
